// 设置显示页和列
static void OLED_SetPos(uint8_t x, uint8_t y) {
    Soft_I2C_WriteCmd(OLED_ADDR, 0xB0 + y);
    Soft_I2C_WriteCmd(OLED_ADDR, ((x & 0xF0) >> 4) | 0x10);
    Soft_I2C_WriteCmd(OLED_ADDR, x & 0x0F);
}

void OLED_Init(void) {
    Soft_I2C_Init();
    HAL_Delay(100);

    Soft_I2C_WriteCmd(OLED_ADDR, 0xAE); // 关闭显示
    Soft_I2C_WriteCmd(OLED_ADDR, 0x20); // 设置内存地址模式
    Soft_I2C_WriteCmd(OLED_ADDR, 0x10); // 页地址模式
    Soft_I2C_WriteCmd(OLED_ADDR, 0xB0); // 页起始地址
    Soft_I2C_WriteCmd(OLED_ADDR, 0xC8); // COM 扫描方向
    Soft_I2C_WriteCmd(OLED_ADDR, 0x00); // 列低位
    Soft_I2C_WriteCmd(OLED_ADDR, 0x10); // 列高位
    Soft_I2C_WriteCmd(OLED_ADDR, 0x40); // 显示起始行
    Soft_I2C_WriteCmd(OLED_ADDR, 0x81); // 对比度设置
    Soft_I2C_WriteCmd(OLED_ADDR, 0xFF); // 最大对比度
    Soft_I2C_WriteCmd(OLED_ADDR, 0xA1); // 左右翻转
    Soft_I2C_WriteCmd(OLED_ADDR, 0xA6); // 正常显示
    Soft_I2C_WriteCmd(OLED_ADDR, 0xA8); // 多路复用比
    Soft_I2C_WriteCmd(OLED_ADDR, 0x3F);
    Soft_I2C_WriteCmd(OLED_ADDR, 0xA4); // 全局显示开启
    Soft_I2C_WriteCmd(OLED_ADDR, 0xD3); // 显示偏移
    Soft_I2C_WriteCmd(OLED_ADDR, 0x00);
    Soft_I2C_WriteCmd(OLED_ADDR, 0xD5); // 时钟分频
    Soft_I2C_WriteCmd(OLED_ADDR, 0xF0);
    Soft_I2C_WriteCmd(OLED_ADDR, 0xD9); // 预充电周期
    Soft_I2C_WriteCmd(OLED_ADDR, 0x22);
    Soft_I2C_WriteCmd(OLED_ADDR, 0xDA); // COM 引脚配置
    Soft_I2C_WriteCmd(OLED_ADDR, 0x12);
    Soft_I2C_WriteCmd(OLED_ADDR, 0xDB); // VCOMH 电压
    Soft_I2C_WriteCmd(OLED_ADDR, 0x20);
    Soft_I2C_WriteCmd(OLED_ADDR, 0x8D); // 电荷泵
    Soft_I2C_WriteCmd(OLED_ADDR, 0x14); // 开启电荷泵
    Soft_I2C_WriteCmd(OLED_ADDR, 0xAF); // 开启显示

    OLED_Clear();
}

void OLED_Clear(void) {
    for (uint8_t page = 0; page < 8; page++) {
        OLED_SetPos(0, page);
        for (uint8_t col = 0; col < 128; col++) {
            Soft_I2C_WriteData(OLED_ADDR, 0x00);
        }
    }
}

void OLED_ShowString(uint8_t x, uint8_t col, char *str) {
    OLED_SetPos(col * 6, x);
    while (*str) {
        uint8_t c = *str - 32;
        if (c >= sizeof(Font6x8) / 6) c = 0;
        for (int i = 0; i < 6; i++) {
            Soft_I2C_WriteData(OLED_ADDR, Font6x8[c][i]);
        }
        str++;
    }
}

void OLED_ShowNumber(uint8_t x, uint8_t col, int32_t num) {
    char buf[16];
    sprintf(buf, "%ld", num);
    OLED_ShowString(x, col, buf);
}

void OLED_ShowFloat(uint8_t x, uint8_t col, float val, uint8_t decimals) {
    char buf[16];
    int integer = (int)val;
    int decimal = (int)((val - integer) * 100);
    if (decimal < 0) decimal = -decimal;
    sprintf(buf, "%d.%02d", integer, decimal);
    OLED_ShowString(x, col, buf);
}
